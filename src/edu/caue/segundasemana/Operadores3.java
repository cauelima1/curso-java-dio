package edu.caue.segundasemana;

public class Operadores3 {
    public static void main(String[] args) {
        int numero = 5;

        numero ++;

        System.out.println("O numero e: " + numero);
        
        System.out.println(++ numero);
        System.out.println(numero ++);
        System.out.println(numero);
        System.out.println(numero --);
        System.out.println(numero);
        System.out.println(-- numero);
    }
    
}
